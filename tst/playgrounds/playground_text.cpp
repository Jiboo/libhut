  win.on_resize.connect(relayout);

  dsp.dispatch();

#ifdef HUT_TEXT_SAMPLE_ENABLE_IMGUI
  ImGui_ImplHut_Shutdown();
  ImGui::DestroyContext();
#endif

  return EXIT_SUCCESS;
